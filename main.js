function buildSymbolCatalog() {
  const catalog = [];
  symbolBlocks.forEach((block) => {
    if (!block.ranges) return;
    block.ranges.forEach(([start, end]) => {
      for (let code = start; code <= end; code += 1) {
        const char = String.fromCodePoint(code);
        catalog.push({
          char,
          code,
          block: block.id,
          label: block.label
        });
      }
    });
  });
  return catalog;
}

